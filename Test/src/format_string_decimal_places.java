
public class format_string_decimal_places 
{
	public static void main(String args[]) 
	{
		System.out.print(String.format("is: %.1f\n" ,5.635536666643));
		System.out.print(String.format("is: %.2f\n" ,5.635536666643));
		System.out.print(String.format("is: %.3f\n" ,5.635536666643));
		System.out.print(String.format("is: %.4f\n" ,5.635536666643));
		System.out.print(String.format("is: %.5f\n" ,5.635536666643));
	}

}
